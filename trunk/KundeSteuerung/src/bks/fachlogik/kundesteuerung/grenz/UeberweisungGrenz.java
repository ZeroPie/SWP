package bks.fachlogik.kundesteuerung.grenz;

import java.util.Date;

public class UeberweisungGrenz {
    
    private int     ubid;
    private double  betrag;
    private Date    datum;
    private String  verwendungszweck;
    private int     vonkonto;
    private int     zukonto;
    private String  status;
    private int     kid;

    public int getUbid() {
        return ubid;
    }

    public void setUbid(int ubid) {
        this.ubid = ubid;
    }

    public double getBetrag() {
        return betrag;
    }

    public void setBetrag(double betrag) {
        this.betrag = betrag;
    }

    public Date getDatum() {
        return datum;
    }

    public void setDatum(Date datum) {
        this.datum = datum;
    }

    public String getVerwendungszweck() {
        return verwendungszweck;
    }

    public void setVerwendungszweck(String verwendungszweck) {
        this.verwendungszweck = verwendungszweck;
    }

    public int getVonkonto() {
        return vonkonto;
    }

    public void setVonkonto(int vonkonto) {
        this.vonkonto = vonkonto;
    }

    public int getZukonto() {
        return zukonto;
    }

    public void setZukonto(int zukonto) {
        this.zukonto = zukonto;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getKid() {
        return kid;
    }

    public void setKid(int kid) {
        this.kid = kid;
    }
}
