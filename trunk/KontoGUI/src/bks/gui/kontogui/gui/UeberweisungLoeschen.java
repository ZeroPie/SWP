    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        int[] rows = this.tableUeberweisungen.getSelectedRows();
        ArrayList<UeberweisungGrenz> uList = new ArrayList<>();
        for(int i : rows){
            UeberweisungGrenz uG = this.ueberList.get(i);
            uList.add(uG);
        }
        
        if(this.kontoSteuerung.ueberweisungenLoeschen(uList)){
            JOptionPane.showMessageDialog(this, "Überweisungen erfolgreich gelöscht.", "Erfolg", JOptionPane.INFORMATION_MESSAGE);
            this.initTable();
        }else {
            JOptionPane.showMessageDialog(this, "Es ist ein Fehler beim Löschen aufegtreten.", "Fehler", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteMouseClicked

