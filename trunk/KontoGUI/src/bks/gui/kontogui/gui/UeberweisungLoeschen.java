    private void initTable(){
        String[] titles = {"Id", "Betreff", "Date", "Status"};
        
        this.ueberList = this.kontoSteuerung.getLoeschbareUeberweisungen();
        DefaultTableModel ueberModel = new DefaultTableModel(titles, 0);
        
        for (UeberweisungGrenz u : ueberList) {
            Object[] row = {"" + u.getUbid(), u.getVerwendungszweck(), u.getDatum().toLocaleString(), u.getStatus()};
            ueberModel.addRow(row);
        }
        
        this.tableUeberweisungen.setModel(ueberModel);
    }

